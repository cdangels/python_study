#El bucle While se utiliza para ejecutar un bloque de instrucciones repetidas veces "infinitas" mientras una condición se cumple.
#El bucle While se ejecuta mientras la condición sea realizada, y se ejecuta el bloque de instrucciones dentro del bucle cuando la condición se cumple.
#La diferencia principal entre for y while es que el bucle for se utiliza para iterar sobre una secuencia (como una lista, tupla, cadena de texto, etc.) y el bucle while se utiliza para ejecutar un bloque de instrucciones mientras una condición sea verdadera.

num = 0 #Inicializamos la variable num en 1
while num <5: #Mientras num sea menor que 5, se ejecuta el bloque de instrucciones dentro del bucle.
    print(num)
    num += 1 #Incrementa el valor de num en 1 en cada iteración del bucle
print(num)

